/** "Social" section for the profile — renders the user's social links (icon +
 *  label) and opens each externally. Renders nothing when there are none. */
export default function SocialLinks({ links }: { links: ExternalLink[] }) {
  const { socials } = splitLinks(links);
  const present = SOCIAL_TYPES.filter((t) => socials[t]);
  if (present.length === 0) return null;

  return (
    <div className="px-8 pb-8">
      <h2 className="text-[22px] font-bold text-th-text-primary tracking-tight mb-4">
        Social
      </h2>
      <div className="flex flex-col gap-3">
        {present.map((type) => {
          const meta = SOCIAL_META[type];
          return (
            <button
              key={type}
              onClick={() => openUrl(socials[type]).catch(() => {})}
              title={socials[type]}
              className="flex items-center gap-3 w-fit text-th-text-secondary hover:text-th-text-primary transition-colors"
            >
              <svg viewBox="0 0 24 24" fill="currentColor" className="w-5 h-5">
                <path d={meta.path} />
              </svg>
              <span className="text-[14px] font-semibold">{meta.label}</span>
            </button>
          );
        })}
      </div>
    </div>
  );
}
